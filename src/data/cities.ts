/** Get a city by slug */
export function getCityBySlug(slug: string): City | undefined {
  return cities.find((c) => c.slug === slug);
}

/** Get all cities for a given county */
export function getCitiesByCounty(county: County): City[] {
  return cities.filter((c) => c.county === county);
}

/** Get all cities served by a given branch */
export function getCitiesByBranch(branch: BranchSlug): City[] {
  return cities.filter((c) => c.branch === branch);
}

/** Get all Tier 1 cities (priority content + city×service pages) */
export function getTier1Cities(): City[] {
  return cities.filter((c) => c.tier === 'tier1');
}

/** Get all static paths for Astro [city].astro */
export function getCityPaths() {
  return cities.map((c) => ({ params: { city: c.slug } }));
}
